package com.freemapper.core;

public class XmlMethod {
    private String id;
    private String content;
    private String type;

    private String url;

    private String paramUrl;

    private String paramUrlId;

    private String resultType;

    private String resultParser;

    public String getResultParser() {
        return resultParser;
    }

    public void setResultParser(String resultParser) {
        this.resultParser = resultParser;
    }

    public String getResultType() {
        return resultType;
    }

    public void setResultType(String resultType) {
        this.resultType = resultType;
    }

    public String getParamUrlId() {
        return paramUrlId;
    }

    public void setParamUrlId(String paramUrlId) {
        this.paramUrlId = paramUrlId;
    }

    public String getParamUrl() {
        return paramUrl;
    }

    public void setParamUrl(String paramUrl) {
        this.paramUrl = paramUrl;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    private String index;

    public String getIndex() {
        return index;
    }

    public void setIndex(String index) {
        this.index = index;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
